var root3 *Trie

func init() {
	root3 = &Trie{}
	for code, country := range iso3map {
		root3.Insert(code, country)
	}
}

func Table3Lookup(code string) (string, bool) {
	if code[0] < 'A' || code[0] > 'Z' {
		return "", false
	}
	if code[1] < 'A' || code[1] > 'Z' {
		return "", false
	}
	if code[2] < 'A' || code[2] > 'Z' {
		return "", false
	}

	country := iso3table[code[0]-'A'][code[1]-'A'][code[2]-'A']
	return country, country != ""
}

func Map3Lookup(code string) (string, bool) {
	val, ok := iso3map[code]
	return val, ok
}

func Trie3Lookup(code string) (string, bool) {
	return root3.Find(code)
}
